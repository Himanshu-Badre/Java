package Encapsulation;
//POJO (PLANE OLD JAVA OBJECT) EXample
public class Employee {
		private int id;
		private String name;
		
		public void Setid(int id) {
			this.id=id;
		}
		public void Setname(String name) {
			this.name=name;
		}
		public int getid() {
			return this.id;
		}
		public String getname() {
			return this.name;
		}
}
